#ifndef CPU_PROC_LIST_H
#define CPU_PROC_LIST_H

/**
 * Создает новую задачу (процесс/поток)
 * @param entry_point - указатель на функцию, с которой начнется выполнение кода
 * @param pdir - физический адрес каталога страниц (CR3) для этой задачи
 * @param name - текстовое имя процесса (для отладки и списка задач)
 * @return - указатель на созданную структуру управления задачей (task_t)
 */
task_t* create_task(void (*entry_point)(), uint32_t pdir, const char* name);

/**
 * Завершает процесс по его идентификатору (PID)
 * Используется системной оболочкой (shell) для управления запущенными программами
 * @param pid - уникальный номер процесса, который нужно остановить
 */
void shell_kill_proc(int pid);

#endif
