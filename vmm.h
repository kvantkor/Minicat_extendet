#ifndef VMM_H
#define VMM_H

#include <stdint.h>

// Флаги управления страницей (согласно спецификации x86)
#define PAGE_PRESENT 0x1   // Страница присутствует в памяти
#define PAGE_WRITE   0x2   // Разрешена запись на страницу (Read/Write)
#define PAGE_USER    0x4   // Страница доступна в User Mode (кольцо 3)
#define PAGE_SIZE 4096     // Стандартный размер страницы — 4 Килобайта

/**
 * Инициализирует виртуальную память.
 * Создает базовый каталог страниц ядра и включает пейджинг (устанавливает бит в CR0).
 */
void vmm_init();

/**
 * Связывает виртуальный адрес с физическим фреймом.
 * @param virtual - желаемый виртуальный адрес
 * @param physical - реальный физический адрес в RAM
 * @param flags - комбинация флагов (PRESENT, WRITE, USER)
 */
void vmm_map(uint32_t virtual, uint32_t physical, uint32_t flags);

/**
 * Разрывает связь виртуального адреса с физическим.
 * Помечает страницу как отсутствующую и освобождает физический блок в PMM.
 */
void vmm_unmap(uint32_t virtual);

/**
 * Возвращает текущее значение регистра CR3.
 * CR3 хранит физический адрес активного каталога страниц.
 */
uint32_t vmm_get_directory();

/**
 * Тестовая функция для проверки корректности работы пейджинга.
 * Обычно пытается записать/прочитать данные по мапированному адресу.
 */
void test_vmm(void);

/**
 * Возвращает физический адрес каталога страниц ядра.
 * Используется при создании новых задач, чтобы копировать базовые таблицы ядра.
 */
uint32_t vmm_get_kernel_pd();

#endif
