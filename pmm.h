#ifndef PMM_H
#define PMM_H

#include <stdint.h>

// Размер одной физической страницы (стандарт x86)
#define PAGE_SIZE 4096

/**
 * Заголовок блока памяти в куче (Heap).
 * Находится в памяти непосредственно перед выделенным участком.
 */
typedef struct block_header {
    uint32_t size;             // Размер доступной памяти в этом блоке (без учета заголовка)
    uint32_t is_free;          // Флаг: 1, если блок свободен для использования
    struct block_header* next; // Указатель на следующий блок в связном списке кучи
} block_header_t;

// --- Физический менеджер (PMM) ---
// Работает напрямую с физической оперативной памятью через битмап

// Инициализирует битмап памяти на основе данных от загрузчика (RAM_TOTAL_SIZE)
void pmm_init(void);

// Выделяет один физический фрейм (4 КБ). Возвращает физический адрес
void* pmm_alloc_block(void);

// Выделяет несколько идущих подряд физических фреймов (нужно для DMA или спец. нужд)
void* pmm_alloc_blocks(uint32_t count);

// Помечает физический фрейм как свободный в битмапе
void pmm_free_block(void* addr);

// --- Виртуальный менеджер / Аллокатор ядра (Heap) ---
// Эти функции обеспечивают работу ядра с динамическими данными

// Аналог стандартного malloc: выделяет область памяти произвольного размера в байтах
void* kmalloc(uint32_t size);

// Выделяет целую виртуальную страницу, запрашивая физический блок и мапируя его
void* kmalloc_page(void);

// Освобождает виртуальную страницу и связанный с ней физический блок
void kfree_page(void* virt_addr);

// Аналог стандартного free: помечает блок в куче как свободный для повторного использования
void kfree(void *ptr);

#endif
